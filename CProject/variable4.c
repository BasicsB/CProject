	// 변수: 저장공간
	// 자료형: 변수 안에 담길 데이터의 형태

	// 변수 선언 (공간 생성)
	// 자료형 변수명;

	// 예시
	int num;
	char word;
	double num2;

	// 변수 대입 (값 넣기 = )
	num = 33;
	word = 'a';
	num2 = 3.13;
	num2 = -0.123;		// 또 담으면 전에 있던 값은 삭제되고 들어감
	
	num = 123123;
		printf("%d \n", num);		// 123123

	num = 1151;
	printf("%d \n", num);		// 1151

	// 변수 초기화 (선언과 대입을 동시에)
	int num3 = 119;
	char chr = 'A';

	// 자료형이 적혀 있다 == 공간을 처음 만든다.
	// 자료형이 안적혀 있다 == 변수 안에 있는 값을 사용하겠다. or 변수 안에 있는 값을 변경하겠다.

	int num4 = 4; // 공간을 처음 만듬
	num3 = 4; // 자료형을 안적었다? 이미 있는 변수를 활용하겠다.
	printf("%d \n", num4);		// 자료형을 안적었다? =이 없다? 변수 안에 있는 값을 사용하겠다

	// 대신 문자열(큰 따옴표) 안에서 사용할때는 %d

	int n1, n2, n3;		// 한번에 선언하고 초기화 가능
